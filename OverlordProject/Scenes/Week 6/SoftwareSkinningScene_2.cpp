void SoftwareSkinningScene_2::Initialize()
{
	m_SceneContext.settings.enableOnGUI = true;

	ColorMaterial* pMaterial = MaterialManager::Get()->CreateMaterial<ColorMaterial>();

	auto pRoot = new GameObject();
	m_pBone0 = new BoneObject(pMaterial, 15.0f);
	pRoot->AddChild(m_pBone0);
	m_pBone1 = new BoneObject(pMaterial, 15.0f);
	m_pBone0->AddBone(m_pBone1);

	AddChild(pRoot);
	m_pBone0->CalculateBindPose();

	auto pBoxDrawer = new GameObject();
	m_pMeshDrawer = pBoxDrawer->AddComponent(new MeshDrawComponent(24, true));
	InitializeVertices(15.f);
	AddChild(pBoxDrawer);
}

void SoftwareSkinningScene_2::Update()
{
	if (!m_AutoRotate)
		return;
	
	m_BoneRotation += m_RotationSign * m_RotationSpeed * GetSceneContext().pGameTime->GetElapsed();
	if (m_BoneRotation >= m_MaxAngle || m_BoneRotation <= -m_MaxAngle)
		m_RotationSign *= -1;
	
	m_pBone0->GetTransform()->Rotate(0.f, 0.f, m_BoneRotation, true);
	m_pBone1->GetTransform()->Rotate(0.f, 0.f, -m_BoneRotation * 2.f, true);

	m_pBone0->GetBindPose();


	m_pMeshDrawer->RemoveTriangles();

	for (int i{}; i < m_SkinnedVertices.size(); i += 4)
	{
		QuadPosNormCol quad{};

		for (int j{}; j < 4; ++j)
		{
			XMVECTOR originalVector = XMLoadFloat3(&m_SkinnedVertices[i + j].originalVertex.Position);
			XMMATRIX matrix = XMLoadFloat4x4(&m_pBone0->GetBindPose());
			
			XMVector3TransformCoord(originalVector, matrix);


			//XMVector3TransformCoord()
		}


		quad.Vertex1 = m_SkinnedVertices[i].tranformedVertex;
		quad.Vertex2 = m_SkinnedVertices[i + 1].tranformedVertex;
		quad.Vertex3 = m_SkinnedVertices[i + 2].tranformedVertex;
		quad.Vertex4 = m_SkinnedVertices[i + 3].tranformedVertex;

		m_pMeshDrawer->AddQuad(quad);
	}
	m_pMeshDrawer->UpdateBuffer();

}

