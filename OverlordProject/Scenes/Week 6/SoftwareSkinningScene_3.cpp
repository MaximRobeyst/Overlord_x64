void SoftwareSkinningScene_3::Initialize()
{
	m_SceneContext.settings.enableOnGUI = true;

	ColorMaterial* pMaterial = MaterialManager::Get()->CreateMaterial<ColorMaterial>();

	auto pRoot = new GameObject();
	m_pBone0 = new BoneObject(pMaterial, 15.0f);
	pRoot->AddChild(m_pBone0);
	m_pBone1 = new BoneObject(pMaterial, 15.0f);
	m_pBone0->AddBone(m_pBone1);

	AddChild(pRoot);
	m_pBone0->CalculateBindPose();

	auto pBoxDrawer = new GameObject();
	m_pMeshDrawer = pBoxDrawer->AddComponent(new MeshDrawComponent(24, true));
	InitializeVertices(15.f);
	AddChild(pBoxDrawer);
}

void SoftwareSkinningScene_3::Update()
{
	if (!m_AutoRotate)
		return;

	m_BoneRotation += m_RotationSign * m_RotationSpeed * GetSceneContext().pGameTime->GetElapsed();
	if (m_BoneRotation >= m_MaxAngle || m_BoneRotation <= -m_MaxAngle)
		m_RotationSign *= -1;

	m_pBone0->GetTransform()->Rotate(0.f, 0.f, m_BoneRotation, true);
	m_pBone1->GetTransform()->Rotate(0.f, 0.f, -m_BoneRotation * 2.f, true);

	m_pBone0->GetBindPose();

	m_pMeshDrawer->RemoveTriangles();

	XMVECTOR originalVector{};
	
	XMVECTOR transformedVectorB0{};
	XMVECTOR transformedVectorB1{};
	
	XMVECTOR transformedVector{};

	// Bone 0
	XMMATRIX poseMatrixB0 = XMLoadFloat4x4(&m_pBone0->GetBindPose());
	XMMATRIX worldMatrixB0 = XMLoadFloat4x4(&m_pBone0->GetTransform()->GetWorld());
	XMMATRIX boneTransformB0 = XMMatrixMultiply(poseMatrixB0, worldMatrixB0);
	
	// Bone 1
	XMMATRIX worldMatrixB1 = XMLoadFloat4x4(&m_pBone1->GetTransform()->GetWorld());
	XMMATRIX poseMatrixB1 = XMMatrixInverse(nullptr, XMLoadFloat4x4(&m_pBone1->GetBindPose()));
	XMMATRIX boneTransformB1 = XMMatrixMultiply(poseMatrixB1, worldMatrixB1);

	XMMATRIX currentBoneTransform = boneTransformB0;

	for (int i{}; i < m_SkinnedVertices.size(); i += 4)
	{
		QuadPosNormCol quad{};

		for (int j{}; j < 4; ++j)
		{
			originalVector = XMLoadFloat3(&m_SkinnedVertices[i + j].originalVertex.Position);

			transformedVectorB0 = XMVector3TransformCoord(originalVector, boneTransformB0);
			transformedVectorB1 = XMVector3TransformCoord(originalVector, boneTransformB1);

			transformedVector = XMVectorLerp(transformedVectorB0, transformedVectorB1, m_SkinnedVertices[i + j].blendWeight1); // boneTransformB0* m_SkinnedVertices[i + j].blendWeight0 +

			//transformedVector = XMVector3TransformCoord(originalVector, currentBoneTransform);
			XMStoreFloat3(&m_SkinnedVertices[i + j].tranformedVertex.Position, transformedVector);

		}


		quad.Vertex1 = m_SkinnedVertices[i].tranformedVertex;
		quad.Vertex2 = m_SkinnedVertices[i + 1].tranformedVertex;
		quad.Vertex3 = m_SkinnedVertices[i + 2].tranformedVertex;
		quad.Vertex4 = m_SkinnedVertices[i + 3].tranformedVertex;

		m_pMeshDrawer->AddQuad(quad);
	}
	m_pMeshDrawer->UpdateBuffer();

}

